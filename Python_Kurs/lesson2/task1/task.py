a = b = 2  # Dies wird als "verkettete Zuordnung" bezeichnet. Den Variablen "a" und "b" wird der Wert 2 zugewiesen.
print("a = " + str(a))   # Wir werden den Ausdruck str(a) später im Kurs erklären. Zur Zeit wird sie verwendet, um die Variable "a" in einen String umzuwandeln.
print("b = " + str(b))

