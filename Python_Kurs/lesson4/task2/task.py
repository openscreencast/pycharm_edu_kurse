animals = ['elephant', 'lion', 'tiger', "giraffe"]  # eine neue Liste erstellen
print(animals)

animals += ["monkey", 'dog']    # zwei Einträge zur Liste hinzufügen
print(animals)

animals.append("dino")   # ein oder mehrere Werte zur Liste hinzufügen, benutze die append()-Methode
print(animals)

animals[6] = "dinosaur"
print(animals)
